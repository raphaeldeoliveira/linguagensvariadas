# -var inverte o sinal da variavel
x = 10
y = -10
print(-x)
print(-y)

# podemos fazer divisão inteira com //
print(5//2)

# podemos fazer um numero elevado a outro com **
print(5**2)

# ordem de precedencia nas opereações: *, /, + e -
# w =+ 1 === w = w + 1
w = 10
w += 1
print(w)
w = w + 1
print(w)